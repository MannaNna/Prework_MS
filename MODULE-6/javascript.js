
function myGrowFunction(){

    document.getElementById("box").style.height = 250+'px';
    document.getElementById("box").style.width = 250+'px';

}


function myBlueFunction(){

    document.getElementById("box").style.backgroundColor = "blue";

}


function myFadeFunction(){

    document.getElementById("box").style.opacity = "0.5";
 
}


function myResetFunction(){
    
    document.getElementById("box").style.height = 150+'px';
    document.getElementById("box").style.width = 150+'px';
    document.getElementById("box").style.backgroundColor = "orange";
    document.getElementById("box").style.opacity = "1";

}


